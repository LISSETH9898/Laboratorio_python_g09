"""Tipos de datos trascendentales"""

"""Tipos de datos: Listas []"""

var1 = [20, "Luis", "Calle Las Alamedas", 1993]

print("El valor de mi variable var1 es: {}".format(var1))

"""Tipos de datos: Tuplas ()"""

var2 = [40, 80.9,170, 2024,  "Hola Pythonistas"]

print("El valor de mi variable var2 es como tupla: {}:".format(var2))

"""Tipos de datos: Set"""

var3 = set(["Bienvenido", 50, 150, 40.79, False, 100, 50, 50, 150])

print("El valor de mi variable var3 es como conjunto: {}:".format(var3))

"""Tipos de datos: Diccionario {}"""

var4 = {"nombre": "Mario", "edad": 28, "dni": 12547885}

print("El valor de mi variable var4 es diccionario: {}:".format(var4))